package Sample;

public class Example4 {

    public static void main(String[] args) {

        int i_result, i_rem;
        double d_result, d_rem;
        i_result = 10 / 3;
        i_rem = 10 % 3;
        d_result = 10.0 / 3.0;
        d_rem = 10.0 % 3.0;
        System.out.println("Result and remainder of 10 / 3: " + i_result + " " + i_rem);
        System.out.println("Result and remainder of 10.0 / 3.0: " + d_result + " " + d_rem);
    }
}
